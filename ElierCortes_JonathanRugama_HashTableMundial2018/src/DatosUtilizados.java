
public class DatosUtilizados {

	String claveUtilizada;
	String nombreUtilizado;
	String posicionUtilizada;
	public DatosUtilizados() {
		
	}
	public String getClaveUtilizada() {
		return claveUtilizada;
	}
	public void setClaveUtilizada(String claveUtilizada) {
		this.claveUtilizada = claveUtilizada;
	}
	public String getNombreUtilizado() {
		return nombreUtilizado;
	}
	public void setNombreUtilizado(String nombreUtilizado) {
		this.nombreUtilizado = nombreUtilizado;
	}

	public String getPosicionUtilizada() {
		return posicionUtilizada;
	}
	public void setPosicionUtilizada(String posicionUtilizada) {
		this.posicionUtilizada = posicionUtilizada;
	}
	
	
}
